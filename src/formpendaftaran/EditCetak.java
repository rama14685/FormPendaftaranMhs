    public void print() {
        try {
            PrinterJob pjob = PrinterJob.getPrinterJob();
            pjob.setJobName("Formulir Pendaftaran");
            pjob.setCopies(1);
            pjob.setPrintable(new Printable() {
                public int print(Graphics g1, PageFormat pf, int pageNum) {
                    if (pageNum > 0) {
                        return Printable.NO_SUCH_PAGE; // end of job
                    }

                    Graphics2D g2 = (Graphics2D) g1;
                    g2.translate(pf.getImageableX(), pf.getImageableY());

                    g2.drawString("Pendaftaran Mahasiswa Baru 2024", 50, 50);
                    g2.drawString("Formulir Pendaftaran", 125, 70);

                    try {
                        konekDb rama = new konekDb();
                        Connection con = rama.Buka();
                        String userId = UserSession.getUserId();

                        String strsql = "SELECT f.nodaftar, f.full_name, f.tanggal, f.alamat, f.email, f.nohp, "
                                        + "p.jalur, p.prodi, p.biaya "
                                        + "FROM formdaftar f INNER JOIN prodi p ON f.nodaftar = p.nodaftar "
                                        + "WHERE f.id = ?";
                        PreparedStatement ps = con.prepareStatement(strsql);
                        ps.setString(1, userId);
                        ResultSet rs = ps.executeQuery();

                        int y = 100; // starting y position
                        float lineSpacing = 1.5f; // line spacing factor

                        while (rs.next()) {
                            drawLeftAlignedString(g2, "No. Pendaftaran", rs.getString("nodaftar"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Nama Lengkap", rs.getString("full_name"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Tanggal Lahir", rs.getString("tanggal"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Alamat", rs.getString("alamat"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Email", rs.getString("email"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Nomor Telepon", rs.getString("nohp"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Jalur Pendaftaran", rs.getString("jalur"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Program Studi", rs.getString("prodi"), 50, y);
                            y += 15 * lineSpacing;
                            drawLeftAlignedString(g2, "Total Biaya", "Rp " + NumberFormat.getNumberInstance().format(rs.getInt("biaya")), 50, y);
                            y += 15 * lineSpacing;
                            y += 10; // space between data entries
                        }

                        rs.close();
                        ps.close();
                        con.close();
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                    return Printable.PAGE_EXISTS;
                }
            });

            if (pjob.printDialog() == false) {
                return;
            }
            pjob.print();
        } catch (PrinterException pe) {
            pe.printStackTrace();
        }
    }

    private void drawLeftAlignedString(Graphics2D g2, String label, String value, int x, int y) {
        FontMetrics fm = g2.getFontMetrics();
        int labelWidth = fm.stringWidth(label + ": ");
        g2.drawString(label + ":", x, y); // label
        g2.drawString(value, x + labelWidth + 10, y); // value, dengan penyesuaian posisi
    }

