    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        String userId = UserSession.getUserId();
        try {
            konekDb rama = new konekDb();
            Connection con = rama.Buka();

            // Query untuk memeriksa apakah sudah ada data di tabel formdaftar untuk userId tertentu
            String checkFormDaftarQuery = "SELECT COUNT(*) AS count FROM formdaftar WHERE id = ?";
            PreparedStatement checkFormDaftarStmt = con.prepareStatement(checkFormDaftarQuery);
            checkFormDaftarStmt.setString(1, userId);
            ResultSet formDaftarRs = checkFormDaftarStmt.executeQuery();

            if (formDaftarRs.next() && formDaftarRs.getInt("count") > 0) {
                // Jika data formdaftar ada, lanjutkan memeriksa tabel prodi
                String checkProdiQuery = "SELECT COUNT(*) AS count FROM prodi WHERE nodaftar = (SELECT nodaftar FROM formdaftar WHERE id = ?)";
                PreparedStatement checkProdiStmt = con.prepareStatement(checkProdiQuery);
                checkProdiStmt.setString(1, userId);
                ResultSet prodiRs = checkProdiStmt.executeQuery();

                if (prodiRs.next() && prodiRs.getInt("count") > 0) {
                    // Jika data prodi ada, buka halaman edit
                    this.dispose();
                    Fix editData = new Fix();
                    editData.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Anda belum mengisi form prodi.");
                }

                prodiRs.close();
                checkProdiStmt.close();
            } else {
                JOptionPane.showMessageDialog(this, "Anda belum mengisi form data diri.");
            }

            formDaftarRs.close();
            checkFormDaftarStmt.close();
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_editActionPerformed

